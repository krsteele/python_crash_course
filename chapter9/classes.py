# Exercises for Chapter 9: Classes

# Classes represent real-world things and situations. 
# When you write a class, you define the general behavior that a whole category of objects can have.
# 
# We create objects based on classes. When you create individual objects from a class, 
# each object is automatically equipped with the general behavior; 
# you can then give each object whatever unique trats you desire. 

# Making an object from a class is called instantiation. You work with instances of a class.